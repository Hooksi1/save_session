<?php
// Получение данных о сессии из запроса
$sessionData = $_GET['session'];

// Путь к файлу для сохранения данных о сессии
$filePath = 'session_data.txt';

// Открытие файла для записи (если его нет, он будет создан)
$file = fopen($filePath, 'a');

// Запись данных о сессии в файл
fwrite($file, $sessionData . "\n");

// Закрытие файла
fclose($file);

// Ответ сервера
echo 'Данные о сессии успешно сохранены!';
?>
